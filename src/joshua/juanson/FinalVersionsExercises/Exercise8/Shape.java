package joshua.juanson.FinalVersionsExercises.Exercise8;
public class Shape {
    float x;
    float y;

    public Shape(float x, float y) {
        this.x = x;
        this.y = y;
    }

    public float getX() {
        return x;
    }

    public void setX(float x) {
        this.x = x;
    }

    public float getY() {
        return y;
    }

    public void setY(float y) {
        this.y = y;
    }
}